#include "water.h"

//****************************************************
//初始化水泵模块连接的IO设置
//****************************************************
void Init_Water()
{
  pinMode(WaterIo,OUTPUT);//设置数字IO脚模式，OUTPUT为输出
}

//****************************************************
//水泵开
//****************************************************
void Water_On()
{
  digitalWrite(WaterIo,HIGH);
}

//****************************************************
//水泵关
//****************************************************
void Water_Off()
{
  digitalWrite(WaterIo,LOW);
}
